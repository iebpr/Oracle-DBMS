SELECT E.ENO, E.ENAME, E.JOB, E.MANAGER, E.HIREDATE, E.SALARY, E.COMMISSION,
       DNO, D.DNAME, D.LOC
  FROM EMP E NATURAL JOIN DEPT D
ORDER BY DNO, E.ENO;

-- 실습 8-12
SELECT E.ENO, E.ENAME, E.JOB, E.MANAGER, E.HIREDATE, E.SALARY, E.COMMISSION,
       DNO, D.DNAME, D.LOC
  FROM EMP E JOIN DEPT D USING (DNO)
 WHERE SALARY >= 3000
ORDER BY DNO, E.ENO;

-- 실습 8-13
SELECT E.ENO, E.ENAME, E.JOB, E.MANAGER, E.HIREDATE, E.SALARY, E.COMMISSION,
       E.DNO,
       D.DNAME, D.LOC
  FROM EMP E JOIN DEPT D ON (E.DNO = D.DNO)
 WHERE SALARY <= 3000
ORDER BY E.DNO, ENO;

