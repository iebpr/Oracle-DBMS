SELECT E1.ENO, E1.ENAME, E1.MANAGER,
       E2.ENO AS MANAGER_ENO,
       E2.ENAME AS MANAGER_ENAME
  FROM EMP E1 LEFT OUTER JOIN EMP E2 ON (E1.MANAGER = E2.ENO)
ORDER BY E1.ENO;

-- 실습 8-15
SELECT E1.ENO, E1.ENAME, E1.MANAGER,
       E2.ENO AS MANAGER_ENO,
       E2.ENAME AS MANAGER_ENAME
  FROM EMP E1 RIGHT OUTER JOIN EMP E2 ON (E1.MANAGER = E2.ENO)
ORDER BY E1.ENO, MANAGER_ENO;

